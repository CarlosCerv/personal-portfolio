export type AdminNotificationSeverity = 'info' | 'success' | 'warning' | 'critical'

export type AdminNotification = {
  id: string
  title: string
  message: string
  severity: AdminNotificationSeverity
  createdAt: string
  relativeTime: string
  href?: string
  source: 'database' | 'derived'
}

