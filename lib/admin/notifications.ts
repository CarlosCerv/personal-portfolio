async function fetchStoredNotifications() {
  const supabase = await createClient()
  if (!supabase) return []

  const { data, error } = await supabase
    .from('admin_notifications')
    .select('*')
    .order('created_at', { ascending: false })
    .limit(8)

  if (error || !data) return []

  return withRelativeTime(
    data.map((item: any) => ({
      id: String(item.id),
      title: String(item.title),
      message: String(item.message),
      severity: (item.severity as AdminNotificationSeverity) ?? 'info',
      createdAt: item.created_at ?? new Date().toISOString(),
      href: item.href ?? undefined,
      source: 'database' as const,
    }))
  )
}

export async function getAdminNotifications(): Promise<AdminNotification[]> {
  const stored = await fetchStoredNotifications()
  if (stored.length > 0) return stored

  const supabase = await createClient()
  const derived: Array<Omit<AdminNotification, 'relativeTime'>> = []

  if (!supabase) {
    return withRelativeTime([
      {
        id: 'supabase-missing',
        title: 'Supabase not configured',
        message: 'The panel cannot read leads or dynamic content because Supabase environment variables are missing.',
        severity: 'warning',
        createdAt: new Date().toISOString(),
        href: '/admin',
        source: 'derived',
      },
    ])
  }

  const [{ data: leads }, { data: drafts }, { data: contactMessages }, { data: profile }] = await Promise.all([
    supabase
      .from('diagnostico_leads')
      .select('id,nombre,empresa,created_at,paquete_recomendado,score')
      .order('created_at', { ascending: false })
      .limit(3),
    supabase
      .from('blog_posts')
      .select('id,titulo,estado,updated_at')
      .eq('estado', 'borrador')
      .order('updated_at', { ascending: false })
      .limit(3),
    supabase
      .from('contact_messages')
      .select('id,nombre,servicio,created_at')
      .order('created_at', { ascending: false })
      .limit(3),
    supabase
      .from('site_profile')
      .select('updated_at,nombre,titulo')
      .single(),
  ])

  ;(leads ?? []).forEach((lead: any) => {
    derived.push({
      id: `lead-${lead.id}`,
      title: `New lead: ${lead.nombre}`,
      message: `${lead.empresa || 'Project without company'} · ${lead.paquete_recomendado || 'Package TBD'} · score ${lead.score ?? 'N/A'}`,
      severity: 'success',
      createdAt: lead.created_at ?? new Date().toISOString(),
      href: '/admin',
      source: 'derived',
    })
  })

  ;(contactMessages ?? []).forEach((message: any) => {
    derived.push({
      id: `contact-${message.id}`,
      title: `New message: ${message.nombre}`,
      message: `Interested in ${message.servicio || 'general consulting'}. Review and reply from your email.`,
      severity: 'info',
      createdAt: message.created_at ?? new Date().toISOString(),
      href: '/admin',
      source: 'derived',
    })
  })

  const draftItems = drafts ?? []

  if (draftItems.length > 0) {
    derived.push({
      id: 'drafts-pending',
      title: `${draftItems.length} pending draft(s)`,
      message: 'There is unpublished blog content. You should review it to keep the blog active.',
      severity: 'warning',
      createdAt: draftItems[0]?.updated_at ?? new Date().toISOString(),
      href: '/admin/blog',
      source: 'derived',
    })
  }

  if (!process.env.VERCEL_API_TOKEN || !process.env.VERCEL_PROJECT_ID) {
    derived.push({
      id: 'analytics-missing',
      title: 'Web analytics not configured',
      message: 'Missing VERCEL_API_TOKEN or VERCEL_PROJECT_ID. Dashboard will only show real internal metrics.',
      severity: 'warning',
      createdAt: new Date().toISOString(),
      href: '/admin',
      source: 'derived',
    })
  }

  if (!profile?.updated_at) {
    derived.push({
      id: 'profile-review',
      title: 'Professional profile to review',
      message: 'No recent update of the public profile was found. Verify that the main information is complete.',
      severity: 'warning',
      createdAt: new Date().toISOString(),
      href: '/admin/perfil',
      source: 'derived',
    })
  }

  return withRelativeTime(
    derived
      .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
      .slice(0, 8)
  )
}
