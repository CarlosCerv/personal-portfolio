function clone(value) {
  return JSON.parse(JSON.stringify(value));
}

function getHobbyData(slug) {
  if (!slug || !HOBBY_LIBRARY[slug]) return null;
  return clone(HOBBY_LIBRARY[slug]);
}

function getAllHobbies() {
  return Object.values(HOBBY_LIBRARY).map(({ slug, title, icon }) => ({ slug, title, icon }));
}

